public class PrimitveDatatype {
    public static void main(String[] args) {
        // Here we discuss about the primitve datatypes in Java
        int rollno = 10;
        System.out.println(rollno);

        String firstName = "Parth";
        System.out.println(firstName);
        // Note - The string is not the primitve datatype

        char letter = 'a';
        System.out.println(letter);

        long largeInteger = 1234567891232L;
        System.out.println(largeInteger);

        float decimal = 12.42f;
        System.out.println(decimal);

        double longDecimal = 123.456789656;
        System.out.println(longDecimal);

        // Note - All the decimal values are bydefault of type double, if you want to store them in float then you have to use f at last.
        boolean result = false;
        System.out.println(!result);

        

    }
    
}
